	public void submission() throws IOException {
		score = 540;
		for (int i = 0; i < 9; i++) {
			for (int j = 0; j < 9; j++) {
				if (solution[i][j] != results[i][j]) {
					score -= 10;
				}
			}
		}
		if (score == 0) {
			JOptionPane.showMessageDialog(null, "Overall attempt is incorrect. Try again");
		} else {
			//EndGame Panel
			endGamePanel();
		}
	}

