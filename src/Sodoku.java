	private boolean checkValidInput(String value) {
		if (!value.matches("[1-9]")) {
			//Alert
			JOptionPane.showMessageDialog(null, "The input must be between 1 - 9. Renter value.");
			return false;
		} else {
			return true;
		}
	}
